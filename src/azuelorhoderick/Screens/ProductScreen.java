    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed
        // TODO add your handling code here:
        int selectedRow = product_tbl.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row to update!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Get table model
    javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) product_tbl.getModel();

    // Update the selected row with values from fields
    model.setValueAt(productID_txt.getText(), selectedRow, 0);
    model.setValueAt(productName_txt.getText(), selectedRow, 1);
    model.setValueAt(category_cmb.getSelectedItem().toString(), selectedRow, 2);
    model.setValueAt(description_txt.getText(), selectedRow, 3);
    model.setValueAt(costPrice_txt.getText(), selectedRow, 4);
    model.setValueAt(sellingPrice_txt.getText(), selectedRow, 5);
    model.setValueAt(quantity_txt.getText(), selectedRow, 6);
    model.setValueAt(reorder_txt.getText(), selectedRow, 7);
    model.setValueAt(supplier_txt.getText(), selectedRow, 8);
    model.setValueAt(unitMeasure_cmb.getSelectedItem().toString(), selectedRow, 9);
    model.setValueAt(productStat_cmb.getSelectedItem().toString(), selectedRow, 10);

    JOptionPane.showMessageDialog(this, "Product updated successfully!");
    
    // ✅ CLEAR FIELDS AFTER UPDATE
    productID_txt.setText("");
    productName_txt.setText("");
    description_txt.setText("");
    costPrice_txt.setText("");
    sellingPrice_txt.setText("");
    quantity_txt.setText("");
    reorder_txt.setText("");
    supplier_txt.setText("");

    category_cmb.setSelectedIndex(0);
    unitMeasure_cmb.setSelectedIndex(0);
    productStat_cmb.setSelectedIndex(0);
    }//GEN-LAST:event_update_btnActionPerformed

