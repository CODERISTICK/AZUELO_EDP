    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        // TODO add your handling code here:
        javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) product_tbl.getModel();

    // Get values from fields
    String productID = productID_txt.getText();
    String productName = productName_txt.getText();
    String category = category_cmb.getSelectedItem().toString();
    String description = description_txt.getText();
    String costPrice = costPrice_txt.getText();
    String sellingPrice = sellingPrice_txt.getText();
    String quantity = quantity_txt.getText();
    String reorder = reorder_txt.getText();
    String supplier = supplier_txt.getText();
    String unitMeasure = unitMeasure_cmb.getSelectedItem().toString();
    String productStatus = productStat_cmb.getSelectedItem().toString();

    // Simple validation (optional)
    if (productID.isEmpty() || productName.isEmpty()) {
        JOptionPane.showMessageDialog(
            this,
            "Product ID and Product Name are required!",
            "Error",
            JOptionPane.ERROR_MESSAGE
        );
        return;
    }

    // Add all data to table
    model.addRow(new Object[] {
        productID,
        productName,
        category,
        description,
        costPrice,
        sellingPrice,
        quantity,
        reorder,
        supplier,
        unitMeasure,
        productStatus
    });

    // Clear all input fields after adding
    productID_txt.setText("");
    productName_txt.setText("");
    description_txt.setText("");
    costPrice_txt.setText("");
    sellingPrice_txt.setText("");
    quantity_txt.setText("");
    reorder_txt.setText("");
    supplier_txt.setText("");

    category_cmb.setSelectedIndex(0);
    unitMeasure_cmb.setSelectedIndex(0);
    productStat_cmb.setSelectedIndex(0);

    // Focus back to Product ID for next entry
    productID_txt.requestFocus();
    }//GEN-LAST:event_add_btnActionPerformed

